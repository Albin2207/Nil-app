class Video {
  final String id;
  final String title;
  final String channel;
  final String channelAvatar;
  final String views;
  final String time;
  final String duration;
  final String thumbnail;

  Video({
    required this.id,
    required this.title,
    required this.channel,
    required this.channelAvatar,
    required this.views,
    required this.time,
    required this.duration,
    required this.thumbnail,
  });
}
